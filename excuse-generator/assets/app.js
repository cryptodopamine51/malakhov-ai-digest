let currentExcuse = '';
let lastIndexes = {};
let toastTimer = 0;

function randomFrom(items, bucket) {
  if (!items.length) return '';

  let index = Math.floor(Math.random() * items.length);
  if (items.length > 1 && lastIndexes[bucket] === index) {
    index = (index + 1) % items.length;
  }

  lastIndexes[bucket] = index;
  return items[index];
}

function getFormData() {
  const data = new FormData(form);
  return {
    situation: data.get('situation') || 'work',
    tone: data.get('tone') || 'calm',
    boldness: Number(data.get('boldness') || 3),
  };
}

function composeExcuse() {
  const { situation, tone, boldness } = getFormData();
  const profile = boldnessProfiles[boldness] || boldnessProfiles[3];
  const scenario = scenarios[situation] || scenarios.work;
  const toneData = tonePreambles[tone] || tonePreambles.calm;
  const level = String(boldness);
  const preamble = randomFrom(toneData.byLevel[boldness] || [''], `${tone}-${level}-preamble`);
  const message = randomFrom(scenario.messages[boldness], `${situation}-${level}-message`);
  let text = [preamble, message].filter(Boolean).join(' ');
  if (boldness === 5 && !PROFANITY_PATTERN.test(text)) {
    text += ' 袧邪褏械褉 泻褉邪褋懈胁褘械 芯锌褉邪胁写邪薪懈褟.';
  }
  const score = getScore(scenario.baseScore, toneData.score, profile.scorePenalty, boldness);

  return {
    scenario,
    profile,
    text,
    score,
    caption: profile.caption,
  };
}

function getScore(baseScore, toneScore, penalty, boldness) {
  const jitter = Math.floor(Math.random() * 7) - 3;
  const score = baseScore + toneScore - penalty + jitter;

  if (boldness === 5) {
    return Math.max(14, Math.min(42, score));
  }

  return Math.max(18, Math.min(96, score));
}

function renderExcuse() {
  const result = composeExcuse();
  currentExcuse = result.text;
  textNode.textContent = result.text;
  scenarioLabel.textContent = result.scenario.label;
  scoreValue.textContent = String(result.score);
  scoreFill.style.width = `${result.score}%`;
  scoreCaption.textContent = result.caption;
  boldnessLabel.textContent = result.profile.label;
  heroRisk.textContent = result.profile.risk;
}

async function copyExcuse() {
  if (!currentExcuse) {
    renderExcuse();
  }

  try {
    if (!navigator.clipboard?.writeText) {
      throw new Error('Clipboard API is unavailable');
    }

    await navigator.clipboard.writeText(currentExcuse);
    showToast('小泻芯锌懈褉芯胁邪薪芯. 袘芯褋褋 锌芯褔褌懈 锌芯胁械褉懈谢.');
    temporarilyRenameCopyButton('小泻芯锌懈褉芯胁邪薪芯');
  } catch {
    if (copyWithTextarea(currentExcuse)) {
      showToast('小泻芯锌懈褉芯胁邪薪芯. 袘芯褋褋 锌芯褔褌懈 锌芯胁械褉懈谢.');
      temporarilyRenameCopyButton('小泻芯锌懈褉芯胁邪薪芯');
      return;
    }

    selectCurrentText();
    showToast('孝械泻褋褌 胁褘写械谢械薪. 小泻芯锌懈褉褍泄褌械 胁褉褍褔薪褍褞.');
  }
}

function copyWithTextarea(value) {
  const textarea = document.createElement('textarea');
  textarea.value = value;
  textarea.setAttribute('readonly', '');
  textarea.style.position = 'fixed';
  textarea.style.left = '-9999px';
  textarea.style.top = '0';
  document.body.appendChild(textarea);
  textarea.select();

  let copied = false;
  try {
    copied = document.execCommand('copy');
  } catch {
    copied = false;
  }

  document.body.removeChild(textarea);
  return copied;
}

function selectCurrentText() {
  const selection = window.getSelection();
  const range = document.createRange();
  range.selectNodeContents(textNode);
  selection.removeAllRanges();
  selection.addRange(range);
}

function temporarilyRenameCopyButton(label) {
  const labelNode = Array.from(copyButton.childNodes).find(
    (node) => node.nodeType === Node.TEXT_NODE && node.textContent.trim(),
  );
  if (!labelNode) return;

  const original = labelNode.textContent;
  labelNode.textContent = ` ${label}`;
  window.setTimeout(() => {
    labelNode.textContent = original;
  }, 1400);
}

function showToast(message) {
  window.clearTimeout(toastTimer);
  toast.textContent = message;
  toast.classList.add('is-visible');
  toastTimer = window.setTimeout(() => {
    toast.classList.remove('is-visible');
  }, 2200);
}

